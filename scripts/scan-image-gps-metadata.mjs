import fs from 'node:fs/promises'
import path from 'node:path'
import { execFile } from 'node:child_process'
import { promisify } from 'node:util'

const execFileAsync = promisify(execFile)
const IMAGE_EXTENSIONS = new Set(['.jpg', '.jpeg', '.png', '.webp'])
const DEFAULT_ROOTS = ['public/blog-images', 'public/images', 'public/jobs', 'public/seo-images']

function getArg(name, fallback = '') {
  const prefix = `--${name}=`
  const match = process.argv.find((arg) => arg.startsWith(prefix))
  return match ? match.slice(prefix.length) : fallback
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath)
    return true
  } catch {
    return false
  }
}

async function listImages(rootDirs) {
  const found = []

  async function walk(currentDir) {
    const entries = await fs.readdir(currentDir, { withFileTypes: true })
    for (const entry of entries) {
      const fullPath = path.join(currentDir, entry.name)
      if (entry.isDirectory()) {
        await walk(fullPath)
        continue
      }
      if (entry.isFile() && IMAGE_EXTENSIONS.has(path.extname(entry.name).toLowerCase())) {
        found.push(fullPath)
      }
    }
  }

  for (const rootDir of rootDirs) {
    if (await pathExists(rootDir)) await walk(rootDir)
  }

  return found.sort((a, b) => a.localeCompare(b))
}

async function readMacMetadata(filePath) {
  try {
    const { stdout } = await execFileAsync('mdls', [
      '-raw',
      '-name',
      'kMDItemLatitude',
      '-name',
      'kMDItemLongitude',
      filePath,
    ])
    const [latitude = '', longitude = ''] = stdout
      .split('\0')
      .map((value) => value.trim())
      .filter(Boolean)

    const hasGps = latitude !== '(null)' && longitude !== '(null)' && latitude !== '' && longitude !== ''

    return {
      hasGps,
      latitude: hasGps ? Number(latitude) : null,
      longitude: hasGps ? Number(longitude) : null,
    }
  } catch (error) {
    return {
      hasGps: false,
      latitude: null,
      longitude: null,
      error: error.message,
    }
  }
}

function toRelative(filePath) {
  return path.relative(process.cwd(), filePath)
}

async function writeJson(outputPath, data) {
  await fs.mkdir(path.dirname(outputPath), { recursive: true })
  await fs.writeFile(outputPath, `${JSON.stringify(data, null, 2)}\n`)
}

async function writeManifest(outputPath, data) {
  const lines = [
    `# Image GPS Located Files Manifest - ${new Date().toISOString().slice(0, 10)}`,
    '',
    'Generated by `node scripts/scan-image-gps-metadata.mjs`.',
    '',
    '| Files scanned | Located files | Not located |',
    '| ---: | ---: | ---: |',
    `| ${data.summary.total} | ${data.summary.located} | ${data.summary.notLocated} |`,
    '',
    '## Located Files',
    '',
    'Coordinates are intentionally omitted from this manifest. Use the JSON output only when exact coordinates are owner-approved for internal review.',
    '',
    '| File | Owner decision |',
    '| --- | --- |',
    ...data.files
      .filter((file) => file.hasGps)
      .map((file) => `| \`${file.path}\` | retain / strip / normalize / leave unchanged |`),
    '',
  ]
  await fs.mkdir(path.dirname(outputPath), { recursive: true })
  await fs.writeFile(outputPath, lines.join('\n'))
}

async function main() {
  const rootArg = getArg('root')
  const rootsArg = getArg('roots')
  const roots = rootsArg
    ? rootsArg.split(',').map((value) => value.trim()).filter(Boolean)
    : rootArg
      ? [rootArg]
      : DEFAULT_ROOTS
  const jsonOutput = getArg('json')
  const manifestOutput = getArg('manifest')

  const existingRoots = []
  for (const root of roots) {
    if (await pathExists(root)) existingRoots.push(root)
  }

  if (existingRoots.length === 0) {
    throw new Error(`No image roots exist: ${roots.join(', ')}`)
  }

  const images = await listImages(existingRoots)
  const files = []

  for (const image of images) {
    const metadata = await readMacMetadata(image)
    files.push({
      path: toRelative(image),
      ...metadata,
    })
  }

  const located = files.filter((file) => file.hasGps).length
  const data = {
    generatedAt: new Date().toISOString(),
    roots: existingRoots,
    summary: {
      total: files.length,
      located,
      notLocated: files.length - located,
    },
    files,
  }

  if (jsonOutput) await writeJson(jsonOutput, data)
  if (manifestOutput) await writeManifest(manifestOutput, data)

  console.log(
    JSON.stringify(
      {
        roots: existingRoots,
        total: data.summary.total,
        located: data.summary.located,
        notLocated: data.summary.notLocated,
        jsonOutput: jsonOutput || null,
        manifestOutput: manifestOutput || null,
      },
      null,
      2,
    ),
  )
}

main().catch((error) => {
  console.error(error.message)
  process.exit(1)
})
